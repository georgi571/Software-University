package _4ProgrammingJavaOOPFebruary2024._1JavaOOP._3Inheritance._2Exersices._01Person;

public class Person {
    private String name;
    private int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
